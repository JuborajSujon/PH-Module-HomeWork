// Expressions in parentheses computed before the rest of the expression function are executed before the result is used in the rest of the expression

/**
 * () - Expression Group - (100+50)*3
 * () - Function call / invock - myFunction()
 * . - Member of object - person.name
 * [] - Member of object - person["name"]
 * ?. - Optional chaining - x?.y
 * new - New with argument - new Date('June 5,2025)
 * new - New without argument - new Date()
 */

